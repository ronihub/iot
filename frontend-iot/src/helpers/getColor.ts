export const getColor = (value: number) => {
  switch (value) {
    case 1:
      return "#9fde8e";
      break;
    case 2:
      return "#b7eba9";
      break;
    case 3:
      return "#ebda91";
      break;
    case 4:
      return "#ebb791";
      break;
    case 5:
      return "#eb9491";
      break;
    default:
    // code block
  }
};
